/**
 * Циклом For вывести
 * горизонтальную линию из 10 воссьмерок
 * вертикальнуюлинию из 10 восьмерок
 */
public class Solution {
    public static void main(String[] args) {
        //write this code
      //  int a = 8;
        for (int i = 1; i < 10; i++) {
            System.out.print('8');
        }
        for (int i = 0; i <= 10; i++) {
            System.out.println('8');
        }
    }
}
